// syntax for while loop 

//   while (condition) {
    //  code to be repeated
// }


// Example programe for while loop
  let i = 0 ;
  while(i <= 5) {
    console.log(i);
    i++;
  }


//   syntax for Do-while loop 

// do {
     // code to be repeated 
// } while (condition);


// example programme for Do-while 
 let a = 0 ;
 do{
    console.log(a);
    a++;
 }while(a < 10);

//  syntax for For loop 

//  for (initialization;condition; changer) {
     // code to be repeated
//  }


// Example programe for For loop
 for(let x = 0; x < 8; x++) {
    console.log(x);
 }